/**
 * LuminaUI v0.3.0 — Component Manifest (100 components, 8 categories).
 *
 * Source of truth for the component generator script and for the
 * playground data layer. Every entry produces:
 *   - a TypeScript Web Component file at src/components/<tag>.ts
 *   - a metadata entry in the playground
 *
 * Each component extends LuminaElement and inherits the shared
 * variant / intensity / theme / accent / speed / depth API.
 *
 * `extraVariants` are variant literals beyond the standard 6
 * (glass, morph, neural, void, aura, dimensional) that the component
 * accepts as valid `variant` attribute values.
 */

export interface ComponentSpec {
  /** Display name (PascalCase, with Lumina prefix). */
  name: string;
  /** Custom element tag name (kebab-case, with lumina- prefix). */
  tag: string;
  /** Category id (1-8). */
  category:
    | 'buttons'
    | 'cards'
    | 'inputs'
    | 'navigation'
    | 'feedback'
    | 'overlays'
    | 'data'
    | 'unique';
  /** Short one-line description. */
  description: string;
  /** Variants the component accepts. */
  variants: string[];
  /** Custom events (besides the standard lumina-* aliases). */
  events?: string[];
  /** CSS parts exposed via `part="..."` in the shadow root. */
  parts: string[];
  /** Component-specific attributes (besides shared LuminaElement ones). */
  props?: Array<{ name: string; type: string; default?: string; description?: string }>;
  /** Slots the component accepts. */
  slots?: string[];
  /** Short visual tagline shown in the playground. */
  tagline: string;
  /** Suggested accent color for the playground demo. */
  accent?: string;
  /** Generation tier — full (rich impl) or stub (template-based). */
  tier: 'full' | 'stub';
}

